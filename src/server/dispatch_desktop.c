static void
find_gl_symbol(void *handle,
               dispatch_t *dispatch,
               FunctionPointerType *slot,
               const char *symbol_name,
               const char *symbol_name_alt1,
               const char *symbol_name_alt2)
{
#if HAS_GL
    FunctionPointerType (*getProcAddress) (const char *procname) = dispatch->glXGetProcAddress;
#else
    FunctionPointerType (*getProcAddress) (const char *procname) = dispatch->eglGetProcAddress;
#endif

    if (getProcAddress) {
        *slot = getProcAddress (symbol_name);
        if (!*slot)
            *slot = getProcAddress (symbol_name_alt1);
        if (!*slot)
            *slot = getProcAddress (symbol_name_alt2);
        return;
    }

    *slot = dlsym (handle, symbol_name);
    if (!*slot)
        *slot = dlsym (handle, symbol_name_alt1);
    if (!*slot)
        *slot = dlsym (handle, symbol_name_alt2);
}

static const char *
dispatch_libgl ()
{
    static const char *default_libgl_name = "libGL.so";
    const char *libgl = getenv ("GPUPROCESS_LIBGL_PATH");
    return ! libgl ? default_libgl_name : libgl;
}

